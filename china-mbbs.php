          <ul class="mt-3">
            <li class="mx-4">
              Select a recognized medical university in China.
            </li>
            <li class="mx-4">
              Verify the university's accreditation (e.g., WHO, NMC, etc.).
            </li>
          </ul>
          <h4 class="mt-xl-3">2. Check Eligibility Criteria</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Academic: Minimum 70% in Physics, Chemistry, Biology (for general
              category).
            </li>
            <li class="mx-4">NEET Exam: Qualify NEET (for Indian students).</li>
          </ul>
          <h4 class="mt-xl-3">3. Submit the Application</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Fill out the application form on the university's official
              website.
            </li>
            <li class="mx-4">
              Attach necessary documents (academic certificates, passport,
              photos, etc.).
            </li>
          </ul>
          <h4 class="mt-xl-3">4. Document Submission</h4>
          <ul class="mt-3">
            <li class="mx-4">10th and 12th-grade mark sheets.</li>
            <li class="mx-4">Valid passport.</li>
            <li class="mx-4">NEET scorecard (for Indian students).</li>
            <li class="mx-4">Passport-sized photos.</li>
            <li class="mx-4">Medical check-up report (if required).</li>
          </ul>
          <h4 class="mt-xl-3">5. Receive the Offer Letter</h4>
          <ul class="mt-3">
            <li class="mx-4">
              The university will issue an offer letter confirming your
              admission after reviewing the application.
            </li>
          </ul>
          <h4 class="mt-xl-3">6. Pay Tuition Fees</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Pay the first-year tuition fees directly to the university’s
              official account.
            </li>
          </ul>
          <h4 class="mt-xl-3">7. Obtain Invitation Letter</h4>
          <ul class="mt-3">
            <li class="mx-4">
              After fee payment, the university sends an official invitation
              letter required for the visa process.
            </li>
          </ul>
          <h4 class="mt-xl-3">8. Apply for a Student Visa</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Submit a visa application to the Chinese embassy with required
              documents (invitation letter, passport, medical certificate,
              etc.).
            </li>
          </ul>
          <h4 class="mt-xl-3">9. Receive Visa</h4>
          <ul class="mt-3">
            <li class="mx-4">
              The student visa is generally processed within 4-6 weeks.
            </li>
          </ul>
          <h4 class="mt-xl-3">10. Travel to China</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Book your flight and inform the university about your arrival
              date.
            </li>
          </ul>
          <h4 class="mt-xl-3">11. Complete University Registration</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Register at the university upon arrival and submit original
              documents.
            </li>
          </ul>
          <h4 class="mt-xl-3">12. Medical Check-Up</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Undergo a medical check-up required for the university
              registration process.
            </li>
          </ul>
          <h4 class="mt-xl-3">13. Obtain Residence Permit</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Apply for a residence permit to legally stay in China during your
              studies.
            </li>
          </ul>
          <h4 class="mt-xl-3">14. Start MBBS Program</h4>
          <ul class="mt-3">
            <li class="mx-4">
              Begin classes after completing all formalities. The course
              duration is typically 6 years.
            </li>
          </ul>
