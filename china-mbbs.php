          <h4 class="mt-xl-3">1. Mandatory for Indian Students:</h4>
          <ul class="mt-3" style="text-align: justify">
            <li class="mx-4">
              Indian students who wish to study MBBS abroad, including China,
              must qualify for the NEET (National Eligibility cum Entrance
              Test). The NEET exam ensures that students are eligible to study
              medicine abroad and practice in India after completing their MBBS.
            </li>
          </ul>
          <h4 class="mt-xl-3">2. NEET Score Requirement:</h4>
          <ul class="mt-3" style="text-align: justify">
            <li class="mx-4">
              Students need to score the minimum qualifying marks in NEET, as
              specified each year by the National Medical Commission (NMC).
              These qualifying marks ensure eligibility for MBBS courses abroad.
            </li>
          </ul>
          <h4 class="mt-xl-3">3. No Entrance Exam in China:</h4>
          <ul class="mt-3" style="text-align: justify">
            <li class="mx-4">
              Chinese medical universities do not require students to take
              entrance exams like NEET or MCAT for admission. Admission is
              generally based on academic performance in high school.
            </li>
          </ul>
          <h4 class="mt-xl-3">4. Importance for Returning to India:</h4>
          <ul class="mt-3" style="text-align: justify">
            <li class="mx-4">
              After completing MBBS in China, students must pass the FMGE
              (Foreign Medical Graduates Examination) or NEXT (National Exit
              Test) to practice medicine in India. NEET qualification is a
              prerequisite for appearing in these licensing exams.
            </li>
          </ul>
          <h4 class="mt-xl-3">5. Validity:</h4>
          <ul class="mt-3" style="text-align: justify">
            <li class="mx-4">
              NEET scores are valid for 3 years for students aspiring to pursue
              medical education abroad.
            </li>
          </ul>

